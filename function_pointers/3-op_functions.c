#include "3-calc.h"

/**
 * op_add - add two value
 *
 * @a: first value
 * @b: second value
 * Return: a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - add two value
 *
 * @a: first value
 * @b: second value
 * Return: a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - add two value
 *
 * @a: first value
 * @b: second value
 * Return: a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - add two value
 *
 * @a: first value
 * @b: second value
 * Return: a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - add two value
 *
 * @a: first value
 * @b: second value
 * Return: a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
